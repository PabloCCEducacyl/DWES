<?php
    function sumar(int|float $x, int|float $y) : int|float {
        return $x + $y;
    }
    
    function restar(int|float $x, int|float $y) : int|float {
        return $x - $y;
    }
    
    function multiplicar(int|float $x, int|float $y) : int|float {
        return $x * $y;
    }
    
    function dividir(int|float $x, int|float $y) : int|float {
        return $x / $y;
    }
    